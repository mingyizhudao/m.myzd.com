            </div>
            <div class="pt10">
                <span class="font-w800">不慎被咬伤的处理：</span>一旦被蜱虫咬伤也不必过于恐慌，不少家住农村尤其是林区或者养狗、猫宠物的市民都有被蜱虫咬伤的经历，但真正被感染疾病的几率很低,只要及早发现并及时到医院配合合理的治疗,被蜱虫咬伤后绝大多数不会危及生命。
